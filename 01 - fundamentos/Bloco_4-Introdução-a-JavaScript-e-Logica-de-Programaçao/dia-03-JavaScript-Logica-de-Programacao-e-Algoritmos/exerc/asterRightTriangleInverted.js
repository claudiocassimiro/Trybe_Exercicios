let n = 5;

for (let i = 1; i <= n; i += 1) {
    switch (i) {
        case 1:
            console.log('    *');
            break;
        case 2:
            console.log('   **');
            break;
        case 3:
            console.log('  ***');
            break;
        case 4:
            console.log(' ****');
            break;
        case 5:
            console.log('*****');
            break;
    }
}